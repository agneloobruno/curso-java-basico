package main.java.com.bruno.cursojava.exercicios34.conversadedeunidadedearea;

public class ConversaoDeUnidadesDeArea {

    public static double converteMetrosParaPes(double metro) {
        return metro * 10.76;
    }

    public static double convertePesParaCentimetros(double ft) {
        return ft * 929;
    }

    public static double converteMilhasParaAcres(double mi) {
        return mi * 640;
    }

    public static double converteAcresParaPes(double ac) {
        return ac * 43.560;
    }
}
